                 Kecil dan Menengah (UMKM) ditunjukkan dengan berbagai cara. 
              </p>
              
              <p>
                  Dalam memulai usaha tersebut, kemudahan yang diberikan seperti menyederhanakan prosedur 
                  perijinan melalui One Single Submission (OSS). Kemudian memberi keringanan biaya perijinan bagi 
                  pembentukan Usaha Kecil dan pembebasan biaya perijinan bagi Usaha Mikro serta dukungan pembiayaan 
                  yang terjangkau bagi Usaha Mikro dan Kecil (UMK).
              </p>
              <p>
                  Saat UMKM sudah terbentuk, dalam mengelolanya, pemerintah juga hadir memberi penyederhanaan administrasi perpajakan, 
                  insentif pajak dan kepabeanan. Selain itu, pemerintah juga menyediakan bantuan dan pendampingan hukum bagi UMK, 
                  pelatihan dan pendampingan sistem aplikasi pembukuan dan pencatatan keuangan UMK.
              </p>
              <p>
                  Untuk upah, ditentukan kesepakatan antara pengusaha dan pekerja minimal sebesar persentase tertentu dari rata-rata konsumsi masyarakat.
                  UMKM diberikan perlindungan agar tidak dikuasai atau dimiliki oleh usaha besar.
                  Pemerintah juga berusaha meningkatkan peluang usaha produk UMKM dengan kemitraan rantai pasok. 
                  
              </p>
              <p>
                 Insentif dan kemudahan kemitraan juga didorong pemerintah agar terbentuk antar usaha menengah dan besar dengan usaha mikro dan kecil.
                 Dalam mengembangkan UMKM, pemerintah memungkinkan kegiatan usaha UMKM menjadi jaminan untuk mengakses kredit pembiayaan usaha.
                 Saat ini, proses pendaftaran Hak Kekayaan Intelektual lebih mudah dan sederhana. Begitu pula untuk impor bahan baku dan 
                 bahan penolong industri dipermudah sekaligus fasilitas ekspor bagi UMKM.
              </p>
              <p>
                  Pemerintah juga mengalokasikan produk UMK dan koperasi dari hasil produksi dalam negeri dalam pengadaan barang/jasa pemerintah. 
                  Selain itu, pemerintah juga memberikan Dana Alokasi Khusus (DAK) untuk mendukung pemberdayaan dan pengembangan UMKM.
                  Produk UMKM berkesempatan lebih besar untuk dipasarkan di rest area jalan tol dan infrastruktur publik seperti terminal, bandara, pelabuhan dan stasiun kereta api.
                  
              </p>
              <p>
                  Dalam fase pengembangan, pemerintah juga dapat memberikan pendampingan bagi pelaku UMKM agar mampu mengakses pembiayaan serta menguatkan kapasitas pelaku usaha pemula. (nr/ds)
